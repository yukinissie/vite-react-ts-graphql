export type UsersQueryVariables = Exact<{ [key: string]: never; }>;


export type UsersQuery = { __typename?: 'Query', users?: Array<{ __typename?: 'User', id: string, name: string, email: string } | null> | null };


export const UsersDocument = gql`
    query Users {
  users {
    id
    name
    email
  }
}
    `;

